package one.protocol.api.model;

public class SocialsNetworks {

    private int id;
    private String name;
    private String url;

    public int getId() {
        return id;
    }

    public SocialsNetworks setId(int id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public SocialsNetworks setName(String name) {
        this.name = name;
        return this;
    }

    public String getUrl() {
        return url;
    }

    public SocialsNetworks setUrl(String url) {
        this.url = url;
        return this;
    }


}
